package com.example.woolky.ui.friends;

public class Friend {
    public String name;
    public String id;

    public Friend(String name, String id)
    {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
